package org.practicas.dry.drycodigo;

public class Utilidad {

  public double getPromedioCalificaciones(double nota1, double nota2) {
    return (nota1 + nota2) / 2;
  }

  public double getPromedioVentas(double venta1, double venta2) {
    return (venta1 + venta2) / 2;
  }

  public double getPromedioEdades(double edad1, double edad2) {
    return (edad1 + edad2) / 2;
  }
}
