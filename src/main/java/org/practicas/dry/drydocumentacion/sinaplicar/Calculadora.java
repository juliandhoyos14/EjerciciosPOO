package org.practicas.dry.drydocumentacion.sinaplicar;

/**
 * Esta clase se llama Calculadora
 * Sirva para realizar operaciones matemáticas básicas
 * Se espera agregar nuevos métodos en el futuro
 */
public class Calculadora {

  /**
   * Este metodo suma dos números
   * @param a El primer número a sumar
   * @param b El segundo número a sumar
   * @return La suma de los dos números
   */
  public int sumarDosNumeros(int a, int b) {
    return a + b; // Suma dos números y retorna el resultado
  }
}
